// Tipe Data Number
// Kalau mau pakai float harus di parse dulu contoh parseFloat
document.writeln("TIPE DATA NUMBER")
document.writeln(("</br>"))
document.writeln(1000)
document.writeln(("</br>"))
document.writeln(1.203030)

//Number Notation
// JavaScript mendukung number notation seperti binary, hexadecimal, dan octal
document.writeln(("</br>"))
document.writeln(0b1111010)
document.writeln(("</br>"))

//Tipe Data Boolean
//Tipe data yang berisikan kebenaran, yaitu benar atau salah
// benar = true
// salah = false
// Boolean akan banyak digunakan diberbagai fitur pemrograman
document.writeln(true)
document.writeln("</br>")
document.writeln(false)
document.writeln('</br>')

//Tipe Data String
document.writeln("String petik dua")
document.writeln('</br>')
document.writeln('string petik satu')

document.writeln('</br>')

//Menambah String atau string concatination
document.writeln("Nama : " + "Gamal")
document.writeln('</br>')
document.writeln('Belajar' + 'JavaScript')
document.writeln('</br>')

//Escape Sequence
//Karakter khusus pada JavaScript
//Contoh:
//\n == ENTER
//\t == TAB
//\' == '
//\" == "
// \\ == \
document.writeln('<textarea cols="100" rows="10">')
document.writeln(('Gamalliel \n Sharon'))
document.writeln('\"Software Engineer \t Remote\"')
document.writeln('</textarea>')